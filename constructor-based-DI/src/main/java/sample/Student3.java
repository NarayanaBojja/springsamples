package sample;

public class Student3 {
	private int rollNo;
	private String name;
	private int i;

	public Student3(int x, int rollNo, String name) {
		super();
		this.rollNo = rollNo;
		this.name = name;
		i = x;
	}

	public int getRollNo() {
		return rollNo;
	}

	public String getName() {
		return name;
	}

	public int getI() {
		return i;
	}
}
